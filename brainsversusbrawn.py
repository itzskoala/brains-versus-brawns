def get_fighter_matchups(fighter_name, results_df):
    """
    Given a fighter's name, return all matchups (as Red or Blue corner).
    """
    mask = (results_df['Red Fighter'] == fighter_name) | (results_df['Blue Fighter'] == fighter_name)
    fighter_matches = results_df[mask].copy()

    # Add a column to indicate whether fighter was red or blue
    fighter_matches['Role'] = fighter_matches.apply(
        lambda row: 'Red' if row['Red Fighter'] == fighter_name else 'Blue', axis=1
    )

    # Add a column to indicate outcome for the fighter
    fighter_matches['Did Win'] = fighter_matches.apply(
        lambda row: row['Actual Winner'] == fighter_name, axis=1
    )

    return fighter_matches.reset_index(drop=True)

