def train_with_prints(model, X_train, y_train, X_test, y_test, loss_fn, optimizer, epochs=80, name="Model"):
    train_losses = []
    test_losses = []

    for epoch in range(epochs):
        # Training
        model.train()
        optimizer.zero_grad()
        pred_train = model(X_train)
        if isinstance(loss_fn, nn.BCELoss):
            pred_train = pred_train.clamp(min=1e-7, max=1 - 1e-7)
        loss_train = loss_fn(pred_train, y_train)
        loss_train.backward()
        optimizer.step()
        train_losses.append(loss_train.item())

        # Testing
        model.eval()
        with torch.no_grad():
            pred_test = model(X_test)
            if isinstance(loss_fn, nn.BCELoss):
                pred_test = pred_test.clamp(min=1e-7, max=1 - 1e-7)
            loss_test = loss_fn(pred_test, y_test)
            test_losses.append(loss_test.item())

        # Print every 5 epochs
        if (epoch + 1) % 10 == 0 or epoch == 0:
            print(f"[{name}] Epoch [{epoch+1}/{epochs}], Train Loss: {loss_train.item():.4f}, Test Loss: {loss_test.item():.4f}")

    return train_losses, test_losses






