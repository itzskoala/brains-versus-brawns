def flip_fighter_row(row):
    flipped = row.copy()
    # Swap red and blue fighter features
    for col in row.index:
        if col.startswith('r_'):
            flipped[col] = row.get('b_' + col[2:], np.nan)
        elif col.startswith('b_'):
            flipped[col] = row.get('r_' + col[2:], np.nan)
    # Flip the winner
    if 'winner' in row:
        if row['winner'] == 'Red':
            flipped['winner'] = 'Blue'
        elif row['winner'] == 'Blue':
            flipped['winner'] = 'Red'
        else:
            flipped['winner'] = row['winner']  # For draws/unknown
    return flipped

