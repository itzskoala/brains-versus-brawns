def predict_hypothetical_matchup(fighter_red, fighter_blue, full_df, feature_columns,
                                 winner_model, method_model, round_model, time_model, save=False):
    """
    Simulates a matchup between two fighters using the trained models and optionally appends to results.
    """
    # Find past fights
    red_fights = full_df[(full_df['r_fighter'] == fighter_red) | (full_df['b_fighter'] == fighter_red)]
    blue_fights = full_df[(full_df['r_fighter'] == fighter_blue) | (full_df['b_fighter'] == fighter_blue)]

    if red_fights.empty or blue_fights.empty:
        raise ValueError("One or both fighters not found in historical data.")

    # Mean of features
    red_features = red_fights[feature_columns].mean()
    blue_features = blue_fights[feature_columns].mean()
    matchup_features = pd.DataFrame([(red_features + blue_features) / 2]).astype(float)

    # Predictions
    pred_winner_label = winner_model.predict(matchup_features)[0]
    pred_method = method_model.predict(matchup_features)[0]
    pred_round = int(np.clip(np.round(round_model.predict(matchup_features)[0]), 1, 5))
    pred_time_sec = int(time_model.predict(matchup_features)[0])
    pred_time_str = seconds_to_min_sec(pred_time_sec)

    predicted_winner = fighter_red if pred_winner_label == 'Red' else fighter_blue

    print("\n🔥 Hypothetical Matchup Prediction 🔥")
    print(f"Red Fighter: {fighter_red}")
    print(f"Blue Fighter: {fighter_blue}")
    print(f"Predicted Winner: {predicted_winner} ({pred_winner_label} corner)")
    print(f"Predicted Method: {pred_method}")
    print(f"Predicted Round: {pred_round}")
    print(f"Predicted Time: {pred_time_str}")

